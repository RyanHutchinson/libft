void	ft_putnbr(int nbr)
{
	if (nbr < 0)
		{
			ft_putchar('-');
			ft_putnbr(-nbr);
		}
	else if (nbr > 9)
		{
			ft_putnbr(nbr / 10);
			ft_putnbr(nbr % 10);
		}
	else
		{
			ft_putchar(nbr + '0');
		}
}
