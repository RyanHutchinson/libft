int ft_atoi(char *str) 
{ 
    int res;
 	int sign;  
    int i;

	res = 0;
	sign = 1;
	i = 0;

	while (str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] != '\0')
	{
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	res = res * sign;
    return (res); 
} 
